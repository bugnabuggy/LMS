using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Threading.Tasks;
using LMS.Core;
using LMS.Core.Models;
using LMS.Infrastructure;
using Microsoft.AspNet.Builder;
using Microsoft.AspNet.Hosting;
using Microsoft.AspNet.Identity.EntityFramework;
using Microsoft.Data.Entity;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Logging;
using LMS.Services;
using Microsoft.Extensions.PlatformAbstractions;

namespace LMS
{
    public class Startup
    {
        public Startup(IHostingEnvironment env)
        {
            // Set up configuration sources.
            var builder = new ConfigurationBuilder()
                .AddJsonFile("appsettings.json")
                .AddJsonFile($"appsettings.{env.EnvironmentName}.json", optional: true);

            if (env.IsDevelopment())
            {
                // For more details on using the user secret store see http://go.microsoft.com/fwlink/?LinkID=532709
                builder.AddUserSecrets();
            }

            builder.AddEnvironmentVariables();
            Configuration = builder.Build();
        }

        public IConfigurationRoot Configuration { get; set; }

        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            var path = PlatformServices.Default.Application.ApplicationBasePath;
            services.AddEntityFramework()
                .AddSqlite()
                .AddDbContext<ModelContext>(opt => opt.UseSqlite("Filename=" + Path.Combine(path, "site.db")));
            // Add framework services.
            //services.AddEntityFramework()
            //    .AddSqlServer()
            //    .AddDbContext<ApplicationDbContext>(options =>
            //        options.UseSqlServer(Configuration["Data:DefaultConnection:ConnectionString"]));

            services.AddIdentity<ApplicationUser, IdentityRole>()
                .AddEntityFrameworkStores<ModelContext>()
                .AddDefaultTokenProviders();

            services.AddMvc();

            // Add application services.
            services.AddTransient<IEmailSender, AuthMessageSender>();
            services.AddTransient<ISmsSender, AuthMessageSender>();

            //Repos
            services.AddScoped<IRepository<UserArea>, Repository<UserArea>>();
            services.AddScoped<IRepository<Goal>, Repository<Goal>>();
            services.AddScoped<IRepository<GoalState>, Repository<GoalState>>();
            services.AddScoped<IRepository<User>, Repository<User>>();
            services.AddScoped<IRepository<CalendarTask>, Repository<CalendarTask>>();

            services.AddScoped<IUserAreaService, UserAreaService>();
            services.AddScoped<IGoalService, GoalService>();
            services.AddScoped<ICalendarTaskService, CalendarTaskService>();
            services.AddScoped<IChartService, ChartService>();

            services.AddScoped<ITimeConverter, TimeConverter>();
            services.AddScoped<IAppContext, Services.AppContext>();
            services.AddScoped<IUnitOfWorkFactory, UnitOfWorkFactory>();

            services.AddTransient<InitData, InitData>();

            //Seed database
            services.BuildServiceProvider().GetService<InitData>().InitializeDatabase();
        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IHostingEnvironment env, ILoggerFactory loggerFactory)
        {
            loggerFactory.AddConsole(Configuration.GetSection("Logging"));
            loggerFactory.AddDebug();

            //if (env.IsDevelopment())
            //{
            //    //app.UseBrowserLink();
            //    //app.UseDeveloperExceptionPage();
            //    //app.UseDatabaseErrorPage();
            //}
            //else
            {
                app.UseExceptionHandler("/Home/Error");


                //from block above
                app.UseDeveloperExceptionPage();
                app.UseDatabaseErrorPage();

                // For more details on creating database during deployment see http://go.microsoft.com/fwlink/?LinkID=615859
                try
                {
                    using (var serviceScope = app.ApplicationServices.GetRequiredService<IServiceScopeFactory>()
                        .CreateScope())
                    {
                        serviceScope.ServiceProvider.GetService<ModelContext>()
                             .Database.Migrate();
                    }
                }
                catch { }
            }

            //app.UseIISPlatformHandler(options => options.AuthenticationDescriptions.Clear());

            app.UseStaticFiles();

            app.UseIdentity();

            // To configure external authentication please see http://go.microsoft.com/fwlink/?LinkID=532715

            app.UseMvc(routes =>
            {
                routes.MapRoute(
                    name: "default",
                    template: "{controller=Home}/{action=Index}/{id?}");
            });
        }

        // Entry point for the application.
        public static void Main(string[] args) => WebApplication.Run<Startup>(args);
    }
}
